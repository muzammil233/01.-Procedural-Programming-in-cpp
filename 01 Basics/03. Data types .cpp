// A program that shows what type of data is stored  by each data type.
#include<iostream>
using namespace std;
int main()
{
    int age = 20;
    float height = 5.7;
    char grade = 'A';
    
    cout<<" Age : "<<age<<endl;
    cout<<" Height : "<<height <<endl;
    cout<<" Grade : "<<grade;
    
    return 0;
}